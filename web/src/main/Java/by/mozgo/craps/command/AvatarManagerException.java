public class AvatarManagerException extends Exception {

    public AvatarManagerException() {
        super();
    }

    public AvatarManagerException(String message) {
        super(message);
    }

    public AvatarManagerException(String message, Throwable cause) {
        super(message, cause);
    }

    public AvatarManagerException(Throwable cause) {
        super(cause);
    }
}